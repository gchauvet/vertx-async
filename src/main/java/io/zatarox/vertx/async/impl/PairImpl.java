package io.zatarox.vertx.async.impl;

import io.zatarox.vertx.async.api.Pair;
import java.io.Serializable;

public final class PairImpl<K, V> implements Pair<K, V>, Serializable {

    private final K key;
    private final V value;

    public PairImpl(K key, V value) {
        this.key = key;
        this.value = value;
    }

    @Override
    public K getKey() {
        return key;
    }

    @Override
    public V getValue() {
        return value;
    }

}
